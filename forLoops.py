# A string is not one value but an array of string values
name = 'Mansur'
# Mansur has 6 elements is the value
for element in name:
    print(element)

list_a = ['Mansur', 'Briana', 'Lut']

for name in list_a:
    print(name)

list_b = [1,2,3,4,5]

# This adds 5 to each element in list_b
for number in list_b:
    print(number + 5)

# To get 5 printed for each element is:
for number in list_b:
    print(5)